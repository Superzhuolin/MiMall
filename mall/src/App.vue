<template>
  <div id="app">
    <router-view></router-view>
  </div>
</template>

<script>
export default {
  name: "App",
  components: {},

  data() {
    return {};
  },
  mounted() {
    this.getUser();
    this.getCartCount();
  },
  methods: {
    getUser() {
      this.axios.get("/user").then((res) => {
        this.$store.dispatch("saveUserName", res.username); //刷新后再次保存用户名
      });
    },
    getCartCount() {
      this.axios.get("/carts/products/sum").then((res) => {
        this.$store.dispatch("saveCartCount", res); //刷新后再次保存用户名
      });
    },
  },
};
</script>
